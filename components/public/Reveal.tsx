"use client";

import { motion } from "framer-motion";

/**
 * Subtle scroll-reveal wrapper. Fades + lifts its children into view the first
 * time they're scrolled to. Server-rendered block markup is passed as children,
 * so the renderer can stay a Server Component.
 */
export default function Reveal({
  children,
  delay = 0,
  className,
}: {
  children: React.ReactNode;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      className={className}
      initial={{ opacity: 0, y: 16 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-80px" }}
      transition={{ duration: 0.5, ease: [0.22, 1, 0.36, 1], delay }}
    >
      {children}
    </motion.div>
  );
}
